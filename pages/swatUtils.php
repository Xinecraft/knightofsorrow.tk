/**
 * A Function that return the Elem name as per parameter from ID
 * @param $id
 * @param $Elem
 * @return string
 *
 * Usage:
 *  GetElemfromId(12,'W')  For Weapon
 *  GetElemfromId(43,'M')   For Map
 * GetElemfromId(43,'A')    For Ammo
 */
function GetElemfromId($id,$Elem)
{
    global $EquipmentTitle;
    global $MapTitle;
    global $AmmoTitle;
    $noofweapons =  count($EquipmentTitle);
    $noofmaps = count($MapTitle);
    $noofammo = count($AmmoTitle);
    if($Elem == 'W') {
        $id = abs($id);
        if ($id < $noofweapons) {
            return $EquipmentTitle[$id];
        }
    }
    elseif($Elem == 'M')
    {
        $id = abs($id);
        if ($id < $noofmaps) {
            return $MapTitle[$id];
        }
    }
    elseif($Elem == 'A')
    {
        $id = abs($id);
        if ($id < $noofammo) {
            return $AmmoTitle[$id];
        }
    }
}
